 					<br>
 					<h3> 2.  Safety & Privacy Considerations / Tips:  </h3>
 					<br>
 					<h4> Personal Information: </h4>
 					<p> •Users need to set up an account to use this app .</p>
                    <p> •Users have to agree to the Terms of Use before using the app. Survivors should read the terms and the accompanying privacy policy  
                         carefully to determine if they want to use the app. </p>
                    <p> .A lot of personal, sensitive information is collected by this app and there is nothing in the policy that explains their data retention policy.</p>
                    <br>
                     <h3>  3.  Location: </h3>
                     <p> •	Users are asked to keep location on at all times. Each time the app is opened, a reminder pops up if location is not turned on. 
                            survivors often turn their location off to minimize privacy and safety risks. Users should only keep location on if they want to use it for this app and can turn it off when they are not using it.</p> 
                      <br>
                      <h3> 4. Communicating with Contacts: </h3>
                      <p> •	When a contact is added into the app, they are immediately sent a notification asking them to accept the terms and be added. Features  
                            cannot be used until invites have been accepted. The invite includes a link and each person invited needs to download the app and create an account. </p>
					  <p> •	When tested, most of the individuals who received the invite didn't realize they were required to do anything and they did not 
						    accept the invite. Others clicked the link and accepted the invite, downloaded the app, but didn't continue to create the account. This left the app completely unusable.</p>
					  <p> •	User is not given an option over when the invites are sent.  They are sent as text messages and are then documented in the phone 
					        log, which could be a trail of the app use that the user doesn't expect. Survivors who use the app may want to delete the sent text message if they are concerned that the abusive person could see it. </p>
					  <p> •	The platform currently does not support Google Voice numbers and will not be able to send text messages to friends who are using   
					        that  as their contact number. </p>
