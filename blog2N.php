					<br>
				<p> It is a sad reflection on our society that women feel unsafe and unprotected not just on deserted roads and car parks, but even in their own homes. Young lawyer Anjali Goyal was attacked and killed by her watchman in what should be the most secure place — her own bedroom! Anjali’s case has shaken the confidence of the bravest of girls. In the aftermath of the horrific news, a single friend asked in distress, “Does that mean we cannot trust any man?”</p>
				    <p> I hate to say this, but yes, it does seem like it. Better safe than sorry… or worse. When work and lifestyles dictate that women need to travel alone, live alone and commute alone, how does a woman ensure her own safety? </p>
				    <p> I think it is important to be prepared for the worst. However dire this may sound, the only way to defend yourself is to take measures against the worst that can happen. Most girls are caught unprepared. Make a note of your most vulnerable moments when you can be overpowered and guard against those. </p>
				    <p> Trust your womanly instincts about people and situations. Be alert to your surroundings and aware of the first signs of danger. If you feel someone is following you, use the trusted contact feature of Informe app through which you can call a friend or relative to escort you back.</p>
				    <p> Feed in the police control room number or 100 into your speed dial. Also have a friend or relative on speed dial. When in vulnerable spots, keep a pepper spray handy. Spray it into the eyes of an assailant. A perfume bottle or hairspray are good alternatives. Supreme Court lawyer Mr. A.k. Antony urges single women to learn self-defence techniques and apply for licenced pistols, to keep in touch with neighbours and call the police patrol at the first sign of danger. “The law is clear that in self-defence you can attack anybody though you must not harm them more than necessary.”</p>
				    <p> Politeness could be your undoing. Never open the door to a stranger when alone nor stop to help another in a deserted area. Call the cops instead. When a mechanic or plumber is due home for work, request a neighbour or friend to be present. </p>
				    <p> Door keys should never be left in obvious places. Once inside, do not leave them next to the main door. Dress appropriately, as per occasion. Clothes that may seem appropriate inside a bar will look provocative when alone in a deserted area. </p>
				    <p> Remember we live in a world of sharp contrasts — the haves and the have-nots, the educated and the uneducated, the cultured and the uncultured. To flash wealth or flesh, or to be perceived to be free with your favours, is to tip the balance, and invite trouble! </p>
